    FK_USER INT NOT NULL,
    FK_SALA VARCHAR(6) NOT NULL,
    
    CONSTRAINT FK_DATA
            FOREIGN KEY (FK_DATA)
            REFERENCES HORARIOS(HORARIO),
    CONSTRAINT FK_USER
            FOREIGN KEY (FK_USER)
            REFERENCES USUARIOS(ID_USER),
    CONSTRAINT FK_SALA
            FOREIGN KEY (FK_SALA)
            REFERENCES SALAS(ID_SALA),
    
